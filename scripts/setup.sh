EOF
        
        echo "âœ… Configured for Email/Password authentication only"
        ;;
        
    2)
        echo "ðŸ”§ Setting up Google OAuth authentication only..."
        
        # Remove email/password components if they exist
        if [ -d "frontend/src/auth/email" ]; then
            rm -rf frontend/src/auth/email
            echo "âœ… Removed Email/Password components"
        fi
        
